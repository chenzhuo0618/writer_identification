%% Instruction for Installation and uninstallation
%
%% Install Automatically
% To install the package, you can simply do the following :
% 
%
% 1. Download (in binary mode) the file
%    ShearLab-PPFT-1.0.zip to your PC.
%
% 2. Extract the .zip file into the folder
%    \matlab\toolbox\.
%
% 3. Remark: If your Matlab root directory is named differently than
%    \matlab then use its correct name (for example \MATLABR12) instead of
%    the \matlab.
%
% 4. On the folder \matlab\toolbox\ShearLab-PPFT-1.0, run the m file ShearLab-PPFT-1.0.m.
%    
%
% 5. Save your current path for your future use.
%
%
%% Install Manually 
% Alternatively, you can install the package manually by the following way:
% 
% 1. Download (in binary mode) the file
%    ShearLab-PPFT-1.0.zip to your PC.
%
% 2. Extract the .zip file into ANY folder you want, say,
%    C:\MyToolbox\.
%
% 3. Manually add the following folders to the searching path in Matlab
%    (see Matlab->File->Set Path).
%
%    ShearLab-PPFT-1.x\PPFTs
%
%    ShearLab-PPFT-1.x\Weightins
%
%    ShearLab-PPFT-1.x\Weighting\Weights
